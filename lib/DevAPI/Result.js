'use strict';

const uint64 = require('../Protocol/Wrappers/ScalarValues/uint64');
const Warnings = require('../../lib/constants/warnings');
const baseResult = require('./BaseResult');
const logger = require('../logger');

const log = logger('api:result');

/**
 * Result set object returned by each database operation.
 * @module Result
 * @mixes module:BaseResult
 */

/**
 * @private
 * @alias module:Result
 * @param {Array<String>} [generatedDocumentIds] - The list of document ids
 * that were auto-generated by the X Plugin for the statement.
 * @param {BigInt} [generatedInsertId] - The first AUTO_INCREMENT value
 * generated by the statement.
 * @param {int64.Type} [integerType] - The convertion mode selected by the
 * application to handle integer values in result sets for the current session.
 * @param {BigInt} [rowsAffected] - The number of rows affected by the statement.
 * @param {Array<Warning>} [warnings] - The list of warnings generated by the
 * statement.
 * @returns {module:Result}
 */
function Result ({ generatedDocumentIds = [], generatedInsertId = 0n, integerType, rowsAffected = 0n, warnings } = {}) {
    return {
        ...baseResult({ warnings }),
        /**
         * Retrieve the number of documents affected by the operation.
         * @function
         * @name module:Result#getAffectedItemsCount
         * @returns {int64} The number of documents.
         */
        getAffectedItemsCount () {
            return uint64.create(rowsAffected, { type: integerType }).valueOf();
        },

        /**
         * Retrieve the number of rows affected by the operation.
         * @function
         * @name module:Result#getAffectedRowsCount
         * @returns {int64} The number of rows.
         * @deprecated since version 8.0.19. Will be removed in future versions. Use {@link module:Result#getAffectedItemsCount|Result.getAffectedItemsCount()} instead.
         */
        getAffectedRowsCount () {
            log.warning('getAffectedRowsCount', Warnings.MESSAGES.WARN_DEPRECATED_RESULT_GET_AFFECTED_ROWS_COUNT, {
                type: Warnings.TYPES.DEPRECATION,
                code: Warnings.CODES.DEPRECATION
            });

            return this.getAffectedItemsCount();
        },

        /**
         * Retrieve the first <code>AUTO INCREMENT</code> value generated by the operation.
         * @function
         * @name module:Result#getAutoIncrementValue
         * @returns {int64} The first value.
         */
        getAutoIncrementValue () {
            return uint64.create(generatedInsertId, { type: integerType }).valueOf();
        },

        /**
         * Retrieve the list of server-side generated document ids.
         * @function
         * @name module:Result#getGeneratedIds
         * @returns {string[]} The list of ids.
         */
        getGeneratedIds () {
            // ids are encoded as V_OCTETS and should be decoded as utf8 strings
            return generatedDocumentIds.map(v => v.toString());
        }
    };
}

module.exports = Result;
